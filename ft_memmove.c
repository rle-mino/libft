#include "libft.h"

void			*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char	*tmp;

	if (len == 0)
		return (dst);
	tmp = (unsigned char *)malloc(sizeof(unsigned char) * len + 1);
	if (tmp == NULL)
		return (NULL);
	ft_memcpy(tmp, src, len);
	ft_memcpy(dst, tmp, len);
	free(tmp);
	return (dst);
}
