int			ft_premiere(int nbr)
{
	int		i;

	if (nbr == 1)
		return (0);
	i = nbr - 1;
	while (i > 1)
	{
		if ((nbr % i) == 0)
			return (0);
		i--;
	}
	return (1);
}
