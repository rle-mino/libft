#include "ft_printf.h"

void				ft_bzero(void *s, size_t n)
{
	size_t			j;
	unsigned char	*d;

	d = (unsigned char*)s;
	j = 0;
	while (j < n)
	{
		d[j] = 0;
		j++;
	}
}
