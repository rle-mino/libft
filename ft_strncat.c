#include "libft.h"

char		*ft_strncat(char *s1, const char *s2, int n)
{
	int		i;
	int		j;

	j = 0;
	i = ft_strlen(s1);
	while (n)
	{
		s1[i] = s2[j];
		j++;
		i++;
		n--;
	}
	i++;
	s1[i] = '\0';
	return (s1);
}
