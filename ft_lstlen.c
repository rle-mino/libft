#include "libft.h"

int				ft_lstlen(t_list *beg)
{
	t_list		*tmp;
	int			i;

	i = 0;
	if (beg == NULL)
		return (0);
	tmp = beg;
	while (tmp)
	{
		tmp = tmp->next;
		i++;
	}
	return (i);
}
